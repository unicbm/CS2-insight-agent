const SUFFIX_KILL_COUNT = /^\d+杀\d+死$/;

/** 返回 tag 的中文说明，找不到返回空字符串。 */
export function describeTag(tag) {
  if (!tag) return "";
  const t = String(tag).trim();
  if (EXACT[t]) return EXACT[t];
  if (SUFFIX_KILL_COUNT.test(t)) return "全局 K/D 数据徽章";
  for (const [p, desc] of PREFIX) {
    if (t.startsWith(p)) return desc;
  }
  return "";
}
